# Test for equality with strings
print('Does "apples" == "apples?')
fruit = 'apples'
print('apples' == fruit)

# Test for inequality with strings
print('\nDo "apples" != "oranges?"')
fruit = 'apples'
print('oranges' != fruit)

# Test using the lower() method
print('\nDoes "Apples" == "apples"?')
fruit = 'apples'
print('Apples' == fruit)
print('\nDoes "Apples".lower() == "apples"?')
print('Apples'.lower() == fruit)

# Numerical test for equality
print("\nDoes 2 + 2 == 4?")
print(2 + 2 == 4)

# Numerical test for inequality
print("\nDoes 2 + 2 != 5?")
print(2 + 2 != 5)

# Numerical test for greater than
print("\nIs 2 > 1?")
print(2 > 1)

# Numerical test for less than
print("\nIs 1 < 2?")
print(1 < 2)

# Numerical test for greater than or equal to
print("\nIs 2 + 2 >= 4?")
print(2 + 2 >= 4)

# Numerical test for less than or equal to
print("\nIs 2 + 2 <= 5?")
print(2 + 2 <= 5)

# Test using the and keyword
print("\nAre 2 + 2 and 2 + 3 > 3?")
print((2 + 2) and (2 + 3) >3 )

# Test using the or keyword
print("\nIs 4 == 2 + 2 or 2 + 3")
print(4 == (2 + 2) or (2 + 3))

